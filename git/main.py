#git: programa de versionamento local e utilizado a ide de sua escolha para programar
#github: plataforma web (site) utilizada como uma rede social de codigos muito utilizado com portfolio de programadores para mostrar o que estao fazendo ou aprendendo
#commit: comando para salvar alterações de arquivos de arquivos (codigos) em um repositor
#repositorio: pasta para receber os codigos de um projeto
#push: envia codigo para um repositor online